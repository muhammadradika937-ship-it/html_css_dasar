let angka = 3;

switch (angka) {
    case 1:
        console.log("satu");
        break;

    case 2:
        console.log("dua");
        break;

    case 3:
        console.log("tiga");
        break;

    case 4:
        console.log("empat");
        break;

    default:
        console.log("Angka yang dimasukkan salah");
}